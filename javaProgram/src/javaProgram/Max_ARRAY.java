package javaProgram;

public class Max_ARRAY {

	public static void main(String[] args) {
		int a[]={40,60,20,70,71};

		int max=0;
		int index=0;

		for(int i=0;i<a.length;i++){

		   if(a[i]>max) {
		      max=a[i]; 
		      index=i;


		}}
		System.out.println(max);
		System.out.println(index);
		
	}
	}

	




